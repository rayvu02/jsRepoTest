// Define a function that handles all the content creation logic
function generateHelloPage() {
  const container = document.getElementById("content-container");

  // Clear any existing loading message
  container.innerHTML = ''; 

  // Create a new H1 element
  const heading = document.createElement("h1");
  heading.textContent = "Hello, Website!";

  // Create a new paragraph element
  const paragraph = document.createElement("p");
  paragraph.textContent = "This content was generated by calling a JavaScript function.";

  // Add the new elements to the page
  container.appendChild(heading);
  container.appendChild(paragraph);
}

// Call the function to build the page when the script runs
generateHelloPage();